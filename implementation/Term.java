package implementation;

/**
 * Represents a term (Fall, Spring, Summer).
 *
 * Term can represent:
 * <ul>
 *     <li>Fall</li>
 *     <li>Spring</li>
 *     <li>Summer</li>
 * </ul>
 */
public enum Term
{
    UNDEFINED,  // Default value
    FALL,       // Fall term
    SPRING,     // Spring term
    SUMMER,     // Summer term
}